    private void ckLembrarPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ckLembrarPasswordActionPerformed
      // O metedo isSelected verifica se o checkbox foi selecionado ou não
        // Verificar se o checkbox foi selecionado,
        // se tiver sido selecionado mostrar a senha caso não ocultar
        if(ckLembrarPassword.isSelected()){
            txtPassword.setEchoChar((char)0);
            txtConfirmarPassword.setEchoChar((char)0);
        }else{
            txtPassword.setEchoChar('*');
            txtConfirmarPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_ckLembrarPasswordActionPerformed

    private void btnContinuarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContinuarActionPerformed
        // TODO add your handling code here:
        
        Empregado emp = new Empregado();
        Date date = new Date(System.currentTimeMillis());  
        String sexo = String.valueOf(cbxSexo.getSelectedItem());
        
        emp.setPrimeiroNome(txtPrimeiroNome.getText());
        emp.setApelido(txtApelido.getText());
        emp.setPrimeiroNome(txtPrimeiroNome.getText());
        emp.setBilheteIdentidade(txtBilheteIdentidade.getText());
        emp.setEmail(txtEmail.getText());
        emp.setNuit(txtNuit.getText());
        emp.setDataCadastro(date);
        emp.setDataNascimento(cbxDataNascimento.getDate());
        emp.setSexo(sexo.charAt(0));    // Converter uma string "M" em um char 'M'
        emp.setNivelDeAcesso(0);  // O Nivel de Acesso '0' coresponde ao SuperAdmin
        emp.setFuncao("Super Admin");  // dar por Deafault a funcao super admin
        //Gerar uma nova senha Aleatoria Para Ca registo.
        
        if(txtPassword.getText().equals(txtConfirmarPassword.getText())){
            emp.setSenha(txtConfirmarPassword.getText());  
            if(salvarASuperAdmin(emp)){
                // Envia um empregado para outra classe!!
                enviaEmpregado(emp);
                this.dispose();
                //DashboardAdmin dashboardAmin = new DashboardAdmin();
                //dashboardAmin.setVisible(true);
            }
        } else{
            JOptionPane.showMessageDialog(this, "Confirmar a sua Senha, as Senhas sao diferentes!!");
        } 
    }//GEN-LAST:event_btnContinuarActionPerformed


    /**
     * 
     * @param emp
     * @return 
     */
    private boolean salvarASuperAdmin(Empregado emp){
        try {
            //Salvar od dados no banco de dados
            this.empregadoDAO.create(emp);
            JOptionPane.showMessageDialog(null, "Super Administrador Cadastrado com sucesso! ");
            return true;  // retorne verdadeiro se o funcionario for gravado com sucesso!!
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao cadastrar o super Administrador");
            e.printStackTrace(); 
            return false; // retorne falso caso o funcionario nao seja gravado!!
        }
            
    }

